function meetAutoReactions() {
  function getElement(xpath) {
    // –æ—Ç—Ä–∏–º—É—î–º–æ –¥–æ—Å—Ç—É–ø –¥–æ –µ–ª–µ–º–µ–Ω—Ç–∞ –Ω–∞ —Å—Ç–æ—Ä—ñ–Ω—Ü—ñ —á–µ—Ä–µ–∑ xpath
    return document.evaluate( //evaluate —Ü–µ –º–µ—Ç–æ–¥ document —Ñ–æ—Ä–º–∞—Ç —Ç–∞–∫–∏–π: (xpathExpression, contextNode, namespaceResolver, resultType, result)
      xpath, // XPath –¥–ª—è –ø–æ—à—É–∫—É –µ–ª–µ–º–µ–Ω—Ç–∞
      document,// –≤–µ—Å—å –¥–æ–∫—É–º–µ–Ω—Ç
      null,// –Ω–µ–º–∞—î namespaceResolver
      XPathResult.FIRST_ORDERED_NODE_TYPE, // –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ –ø–µ—Ä—à–∏–π –∑–Ω–∞–π–¥–µ–Ω–∏–π node
      null,// –ù–µ–º–∞—î –ø–æ–ø–µ—Ä–µ–¥–Ω—ñ—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤
    ).singleNodeValue; // –ü–æ–≤–µ—Ä—Ç–∞—î–º–æ –ø–µ—Ä—à–∏–π –∑–Ω–∞–π–¥–µ–Ω–∏–π –µ–ª–µ–º–µ–Ω—Ç
  }
  function getEmojiButton(index) {
    const emojis = ['üíñ', 'üëç', 'üéâ', 'üëè', 'üòÇ', 'üòÆ', 'üò¢', 'ü§î', 'üëé']; // –ú–∞—Å–∏–≤ –µ–º–æ–¥–∑—ñ, —è–∫—ñ –¥–æ—Å—Ç—É–ø–Ω—ñ –¥–ª—è —Ä–µ–∞–∫—Ü—ñ–π
    return getElement(`//button/img[@data-emoji="${emojis[index]}"]`); /*
    –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ –µ–ª–µ–º–µ–Ω—Ç –µ–º–æ–¥–∑—ñ –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é XPath, –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—á–∏ –∞—Ç—Ä–∏–±—É—Ç data-emoji. –Ü–Ω–¥–µ–∫—Å –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î—Ç—å—Å—è –¥–ª—è –≤–∏–±–æ—Ä—É –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ –µ–º–æ–¥–∑—ñ –∑ –º–∞—Å–∏–≤—É emojis.
    */
  }

  const sendReactionButton = getElement('//i[text()="mood"]'); //–ó–±–µ—Ä—ñ–≥–∞—î –∫–Ω–æ–ø–∫—É –¥–ª—è –≤—ñ–¥–∫—Ä–∏—Ç—Ç—è –ø–∞–Ω–µ–ª—ñ –µ–º–æ–¥–∑—ñ, –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—á–∏ getElement —ñ XPath –¥–ª—è –ø–æ—à—É–∫—É –∫–Ω–æ–ø–∫–∏ –∑ —Ç–µ–∫—Å—Ç–æ–º "mood".
  
  const intervalIds = [];//  –ó–±–µ—Ä—ñ–≥–∞—î —ñd —ñ–Ω—Ç–µ—Ä–≤–∞–ª—ñ–≤, —è–∫—ñ –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—é—Ç—å—Å—è –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω–∏—Ö –∫–ª—ñ–∫—ñ–≤

  return ({
    startReacting(index, timeout = 1000) { //–ü–æ—á–∏–Ω–∞—î –∫–ª—ñ–∫–∏ –Ω–∞ –∫–Ω–æ–ø–∫—É –µ–º–æ–¥–∑—ñ —á–µ—Ä–µ–∑ –∫–æ–∂–Ω—É –∑–∞–¥–∞–Ω—É timeout. –Ø–∫—â–æ –∫–Ω–æ–ø–∫–∞ –µ–º–æ–¥–∑—ñ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–∞, —Å–ø–æ—á–∞—Ç–∫—É –Ω–∞—Ç–∏—Å–∫–∞—î –∫–Ω–æ–ø–∫—É –¥–ª—è –≤—ñ–¥–∫—Ä–∏—Ç—Ç—è –ø–∞–Ω–µ–ª—ñ –µ–º–æ–¥–∑—ñ.
      if (!getEmojiButton(index)) sendReactionButton.click();  // –Ø–∫—â–æ –∫–Ω–æ–ø–∫–∞ –µ–º–æ–¥–∑—ñ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–∞, –Ω–∞—Ç–∏—Å–∫–∞—î–º–æ –∫–Ω–æ–ø–∫—É –¥–ª—è –≤—ñ–¥–∫—Ä–∏—Ç—Ç—è –ø–∞–Ω–µ–ª—ñ –µ–º–æ–¥–∑—ñ
      intervalIds.push(setInterval(() => {// –î–æ–¥–∞—î–º–æ —ñ–Ω—Ç–µ—Ä–≤–∞–ª, —è–∫–∏–π –±—É–¥–µ –∫–ª—ñ–∫–∞—Ç–∏ –Ω–∞ –∫–Ω–æ–ø–∫—É –µ–º–æ–¥–∑—ñ –∫–æ–∂–Ω—É –∑–∞–¥–∞–Ω—É –∫—ñ–ª—å–∫—ñ—Å—Ç—å –º—ñ–ª—ñ—Å–µ–∫—É–Ω–¥ (timeout)
        try {
          getEmojiButton(index).click();   // –ö–ª—ñ–∫–∞—î–º–æ –Ω–∞ –∫–Ω–æ–ø–∫—É –µ–º–æ–¥–∑—ñ
        } catch {
          this.stopReacting();//–£ –≤–∏–ø–∞–¥–∫—É –ø–æ–º–∏–ª–∫–∏ –∑—É–ø–∏–Ω—è—î–º–æ –≤—Å—ñ —Ä–µ–∞–∫—Ü—ñ—ó
        }
      }, timeout));
    },
    stopReacting() {// –ó—É–ø–∏–Ω—è—î –∫–ª—ñ–∫–∏.
      intervalIds.forEach(id => clearInterval(id)); // –ß–∏—Å—Ç–∏–º–æ –º–∞—Å–∏–≤ —ñ–Ω—Ç–µ—Ä–≤–∞–ª—ñ–≤
      intervalIds.length = 0;  
    }
  });
}

const meet = meetAutoReactions(); //—Å—Ç–≤–æ—Ä—é—î –æ–±—î–∫—Ç meet 

meet.startReacting(0);//–ü–æ—á–∏–Ω–∞—î–º–æ –∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω—ñ —Ä–µ–∞–∫—Ü—ñ—ó –∑ —ñ–Ω–¥–µ–∫—Å–æ–º 0
